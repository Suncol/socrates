      subroutine KZZ_SPEC( cal_day, dzzgw )
!----------------------------------------------------------------------
!       Specify a latitude, altitude and day-of-the-year dependent 
!       vertical diffusion coefficient (dzzgw) instead of using Lindzen
!       parameterization.
!----------------------------------------------------------------------
      use GRID_DIMS, only : lmax, niz

      implicit none

!----------------------------------------------------------------------
!       ... Dummy args
!----------------------------------------------------------------------
      real, intent(in) :: cal_day
      real, intent(out) :: dzzgw(lmax,niz)	! m2/s
      
!----------------------------------------------------------------------
!       ... Local variables
!----------------------------------------------------------------------
      integer :: l
      
      do l = 1, lmax
!----------------------------------------------------------------------
!       ... Global average of v5s05a run at 6 jan 1999, cutting the top
!----------------------------------------------------------------------
         dzzgw(l,38:110) = (/    0.41, 0.41, 0.42, 0.42, 0.43, 0.43, 
     $     0.44, 0.44, 0.45, 0.48, 0.52, 0.59, 0.67, 0.77, 0.88, 0.99,
     $     1.10, 1.20, 1.29, 1.38, 1.48, 1.59, 1.70, 1.84, 1.99, 2.17,
     $     2.37, 2.62, 2.92, 3.28, 3.70, 4.20, 4.78, 5.45, 6.20, 7.06,
     $     8.00, 9.04, 10.19, 11.50, 13.01, 14.76, 16.72, 18.85, 21.07,
     $     23.34, 25.68, 28.16, 30.86, 33.84, 37.10, 40.58, 44.22, 
     $     47.96, 51.77, 55.64, 59.56, 63.48, 67.27, 70.67, 73.32,
     $     74.80, 74.77, 73.12, 69.92, 65.48, 60.22, 54.64, 49.27,
     $     44.65, 41.28, 39.58, 39.90 /)
         dzzgw(l,111:niz) = dzzgw(l,110)
      end do
     
      end subroutine KZZ_SPEC
