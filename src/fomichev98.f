      subroutine FOMICHEV98( )
!-----------------------------------------------------------------------
! ... Calculates the CO2 radiative cooling rate (NLTE) following param
!           by Fomichev et al, JGR, Vol.103, D10, pp 11505-11528, 1998
!      Also calculates O3 radiative cooling rate at 9.6 um (LTE)
!             using similar parameterization from routine given by Fomichev
!             The ref for O3 cooling could be Fomichev/Shved:1985
!      Outputs are q3 (for O3) and q4 (for CO2) : radiative cooling 
!              rates (K/day), positive when cool.
!     Programmed by SC - adapted to SOCRATES3 by RK - O3 cooling added by SC
!-----------------------------------------------------------------------
      use SPC_NAMES, only : vid_o3p, vid_co2, vid_o2, vid_o3
      use GRID_DIMS, only : lmax, niz
      use CONC,      only : hm2d
      use BACKATM,   only : Xmsis, msisN2=>N2, wmole, Cp
      use SPECIES,   only : qn2da
      use VEN1,      only : t2d
      use TAU4,      only : q3, q4
      use ZGRID,     only : zgeo
      use PHYS_CST,  only : k, Nav, R0, g0
      use DIAG_CONTROLS, only : debug
      use NUMERICAL, only : SPLINE, SPLINT, INTERP

      implicit none

!-----------------------------------------------------------------------
! ... Parameters: niz0 is 1st alt index such that x_ext(niz0) > 16.5
!           kOqCO2 is quenching rate of CO2* by O(3P); value from Fomichev98
!-----------------------------------------------------------------------
      integer, parameter :: niz0 = 117        ! for SOCRATES only with niz=121
      integer, parameter :: n0 = 8            ! (1:n0) is x = 0. to 1.75
      integer, parameter :: nO3 = n0+35       ! (n0+1:nO3) is x = 2. to 10.5
      integer, parameter :: n1 = n0+43        ! (n0+1:n1) is x = 2. to 12.5
      integer, parameter :: n2 = n1 + 5       ! (n1+1:n2) is x = 12.75 to 13.75
      integer, parameter :: n3 = n2 + 11      ! (n2+1:n3) is x = 14. to 16.5
      real, parameter    :: kOqCO2 = 3.0E-12  ! quenching rate (cm3/s)
      real, parameter    :: A10 = 1.5988      ! Einstein's coeff for 15um CO2
      real, parameter    :: aku = -960.217    ! h*c/k*Vco2 - to get Planck fct
      real, parameter    :: ako3 = -1500.     ! h*c/k*Vo3 - to get Planck fct

!-----------------------------------------------------------------------
! ... Local variables
!-----------------------------------------------------------------------
      real, dimension(niz), save :: pmb, x_ext
      real, dimension(niz) :: t_ext, tm13_ext, lamb_ext
      real, dimension(niz) :: mixO, mixO2, mixN2, mixCO2, mixO3
      real, dimension(niz) :: colCO2, colCO2d
      real, dimension(1:n3), save :: x
      real, dimension(1:n3) :: T, cO, cO2, cN2, cCO2, cO3
      real, dimension(1:n3) :: eps, phi, eps_O3, phi_O3
      real, dimension(1:4) ::
     $                    fix_CO2 = (/ 1.5e-4, 3.6e-4, 4.5e-4, 7.2e-4 /)
      real, dimension(1:4,n0+1:n1,-5:3), save :: a_table, b_table,
     $                                           a2, b2
      real, dimension(n0+1:nO3,-5:3), save :: O3_table 
      real, dimension(n1:n3) :: lamb, ro, mu, Tm13
      real, dimension(n1:n3) :: u, dsmall, epst
      real :: a, b, gtop, logu, L, alpha, Dj, Djm1, h3
      real, dimension(1:51), save :: u_table, L_table, L2
      real, dimension(1:4,n1:n2), save :: u_table_a, alpha_table
      real, dimension(1:4) :: alpha2
      integer, dimension(1) :: locmin, locmax
      integer, dimension(-5:3) ::               ! from table 1
     $                     delj = (/ -25, -12, -7, -3, -1, 0, 1, 3, 6 /)
      integer :: lat, iz, iok, i0, j, indexj
      integer, save :: dumbcount = 0
      logical :: ok = .false.
      logical, save :: entered = .false.

      if( .not. entered ) then
         pmb(:) = (/ (1013.25*EXP( -FLOAT(iz-1)/7. ),iz=1,niz) /)
         x_ext = LOG( 1000. / pmb )
         x(1:n3) = (/ ( 0.25*FLOAT(i0-1), i0 = 1, n3 ) /)
         if( x_ext(niz) <= x(n3) ) then
            stop 'FOMICHEV98: input upper boundary is too low'
         end if
!-----------------------------------------------------------------------
!  ... Get all the tables
!-----------------------------------------------------------------------
         call FOMICHEV98_DATA( )
!-----------------------------------------------------------------------
!  ... Prepare interpolations, calculating second derivatives
!-----------------------------------------------------------------------
         do i0 = n0+1, n1
            do j = -5, 3
               call SPLINE( fix_CO2, a_table(:,i0,j), 4, 1.e32, 1.e32,
     $                      a2(:,i0,j), iok )
               call SPLINE( fix_CO2, b_table(:,i0,j), 4, 1.e32, 1.e32,
     $                      b2(:,i0,j), iok )
            end do
         end do
         call SPLINE( u_table, L_table, 51, 1.e32, 1.e32, L2, iok )
         entered = .true.
      end if

      do lat = 1, lmax
         t_ext(:) = t2d(lat,:)
         mixO(:) = qn2da(lat,:,vid_o3p)
         mixCO2(:) = qn2da(lat,:,vid_co2)
         mixO2(:) = qn2da(lat,:,vid_o2)
         mixN2(:) = Xmsis(lat,:,msisN2)
         mixO3(:) = qn2da(lat,:,vid_o3)

!-----------------------------------------------------------------------
!  ... Calculate CO2 overhead column amount (cm-2)
!-----------------------------------------------------------------------
         gtop = 1.e2 * g0 * ( R0 / (R0+zgeo(lat,niz)) )**2.           ! cm/s2
         colCO2(niz) = hm2d(lat,niz)*mixCO2(niz) * 1.e7 * k * t_ext(niz)
     $               * Nav / ( 44. * gtop )                      ! molec/cm2
         colCO2d(1:niz-1) = 5.e4 * (zgeo(lat,2:niz) - zgeo(lat,1:niz-1))
     $                    * ( hm2d(lat,2:niz)*mixCO2(2:niz)
     $                        + hm2d(lat,1:niz-1)*mixCO2(1:niz-1) )
         do iz = niz-1, 1, -1
            colCO2(iz) = colCO2(iz+1) + colCO2d(iz)
         end do

!-----------------------------------------------------------------------
!  ... Convert input to x vertical grid in 0.25 vertical steps
!-----------------------------------------------------------------------
         call INTERP( n3,x,cCO2, niz,x_ext,mixCO2, spline=.true.,ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: CO2 interpolation failed'
         call INTERP( n3,x,cN2, niz,x_ext,mixN2, spline=.true.,ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: N2 interpolation failed'
         call INTERP(  n3, x, cO, niz, x_ext, mixO, 
     $                 spline=.true., on_log=.true., ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: O(3P) interpolation failed'
         call INTERP( n3,x,cO2, niz,x_ext,mixO2, spline=.true.,ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: O2 interpolation failed'
         call INTERP( n3,x,T, niz,x_ext,t_ext, spline=.true.,ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: Temperature interp failed'
         call INTERP( n3, x, cO3, niz, x_ext, mixO3, ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: O3 interpolation failed'

         phi = EXP( aku / T )     ! eq (6)
         phi_O3 = EXP( ako3 / T )
         eps(:) = 0.
         eps_O3(:) = 0.

!=======================================================================
!  ... The x0 = 2 - 12.5 height range
!           Uses a constant CO2 profile
!=======================================================================
         if( cCO2(1) < fix_CO2(1) ) then
            write(*,'(a,i2,a,es9.3,a)') 'FOMICHEV98 warning: lat= ',lat,
     $          ' ; vmr(CO2) at surf= ',cCO2(1),' is < 150ppm'
            if( debug ) stop 'FOMICHEV98: surf CO2 < 150ppm'
            cCO2(1) = fix_CO2(1)
          else if(  cCO2(1) > fix_CO2(4) ) then
            write(*,'(a,i2,a,es9.3,a)') 'FOMICHEV98 warning: lat= ',lat,
     $          ' ; vmr(CO2) at surf= ',cCO2(1),' is > 720ppm'
            if( debug ) stop 'FOMICHEV98: surf CO2 > 720ppm'
            cCO2(1) = fix_CO2(4)
         end if
         cCO2(1:n1) = cCO2(1)
         do i0 = n0+1, n1
            h3 = 0.
            do j = -5, 3
               indexj = MAX( 1, i0 + delj(j) )
               indexj = MIN( indexj, n3 )
               call SPLINT( fix_CO2, a_table(:,i0,j), a2(:,i0,j), 4,
     $                      cCO2(i0), a, iok )
               call SPLINT( fix_CO2, b_table(:,i0,j), b2(:,i0,j), 4,
     $                      cCO2(i0), b, iok )
               eps(i0) = eps(i0) + ( a + b*phi(i0) )*phi(indexj)       ! eq (5)
               if( i0 <= nO3 ) h3 = h3 + O3_table(i0,j)*phi_O3(indexj)
            end do
            if( i0 <= nO3 ) eps_O3(i0) = h3*cO3(i0)
         end do

!=======================================================================
!  ... For all xj > 12.5 : calculate lambda(xj)
!=======================================================================
         call INTERP( n3-n1+1, x(n1:n3), ro(:), niz, x_ext, hm2d(lat,:), 
     $                spline=.true., on_log=.true., ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: totdens interpolation failed'
         call INTERP( n3-n1+1, x(n1:n3), mu(:), niz,x_ext, wmole(lat,:),
     $                spline=.true., ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: molar mass interp failed'
         Tm13(:) = T(n1:n3)**(-1./3.)
         lamb(:) = A10 / ( A10 + ro(n1:n3) * (
     $      cN2(n1:n3) * ( 5.5e-17 * SQRT(T(n1:n3))
     $                       + 6.7e-10 * EXP(-83.8*Tm13) )
     $      + cO2(n1:n3)
     $          * ( 1.e-15*EXP( 23.37 - 230.9*Tm13 + 564.*Tm13*Tm13 ) )
     $      + cO(n1:n3) * kOqCO2 ) )                             ! eq (8)

!=======================================================================
!  ... The xj = 12.5 - 16.5 height range: get u, L(u), alpha(u), dsmall
!=======================================================================
         call INTERP( n3-n1+1, x(n1:n3), u(n1:n3), niz, x_ext, colCO2,
     $                spline=.true., on_log=.true., ok=ok )              ! to get u
         if( .not. ok ) stop 'FOMICHEV98: CO2 column interp failed'
         do j = n1, n3
            logu = LOG( u(j) )
            if( logu < u_table(1) ) then
               L = 0.
             else if( logu > u_table(51) ) then
               L = L_table(51)
             else
               call SPLINT( u_table, L_table, L2, 51, logu, L, iok ) ! to get L
            end if
            L = EXP( -L )
            if( j <= n2 ) then          ! 12.5<=x(j)<=13.75
               logu = MAX( logu, u_table_a(1,j) )
               logu = MIN( logu, u_table_a(4,j) )
               call SPLINE( u_table_a(:,j), alpha_table(:,j), 4,
     $                      1.e32, 1.e32, alpha2, iok )
               call SPLINT( u_table_a(:,j), alpha_table(:,j), alpha2, 4,
     $                      logu, alpha, iok )                ! to get alpha
             else                                                   ! x(j)>=14
               alpha = 1.
            end if
            dsmall(j) = alpha * L                             ! eq (12)
         end do

!=======================================================================
!  ... The xj = 12.75 - 16.5 height range: cooling rate
!=======================================================================
         epst(n1) = 1.10036e-10 * eps(n1) / ( cCO2(n1) * (1.-lamb(n1)) ) ! eq (10)
         do j = n1+1, n3
            Dj   = 0.25 * ( dsmall(j-1) + 3.*dsmall(j) )            ! eq (11)
            Djm1 = 0.25 * ( 3.*dsmall(j-1) + dsmall(j) )            ! eq (11)
            epst(j) = ( ( 1. - lamb(j-1)*(1.-Djm1) )*epst(j-1)
     $                                  + Djm1*phi(j-1) - Dj*phi(j) )
     $              / ( 1. - lamb(j)*(1.-Dj) )                      ! eq (9)
            eps(j) = 2.63187e11 * cCO2(j) * (1.-lamb(j)) * epst(j)
     $             / mu(j)                                          ! eq (7)
         end do

!-----------------------------------------------------------------------
!  ... Convert output back to input vertical grid
!-----------------------------------------------------------------------
         call INTERP( niz, x_ext, q4(lat,:), n3-n0, x(n0+1:n3),
     $                eps(n0+1:n3), spline=.true., ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: output q4 interpol failed.'
         call INTERP( niz, x_ext, q3(lat,:), nO3-n0, x(n0+1:nO3), 
     $                eps_O3(n0+1:nO3), spline=.true., ok=ok )
         if( .not. ok ) stop 'FOMICHEV98: output q3 interpol failed.'

!=======================================================================
!  ... The xj > 16.5 height range directly in input vertical grid
!           Easy cool-to-space approximation
!=======================================================================
         tm13_ext(niz0:niz) = t_ext(niz0:niz)**(-1./3.)
         lamb_ext(niz0:niz) = A10 / ( A10 + hm2d(lat,niz0:niz)*
     $     ( mixN2(niz0:niz) * ( 5.5e-17 * SQRT(t_ext(niz0:niz))
     $                       + 6.7e-10 * EXP(-83.8*tm13_ext(niz0:niz)) )
     $      + mixO2(niz0:niz)
     $          * ( 1.e-15*EXP( 23.37 - 230.9*tm13_ext(niz0:niz)
     $             + 564.*tm13_ext(niz0:niz)*tm13_ext(niz0:niz) ) )
     $      + mixO(niz0:niz) * kOqCO2 ) )                             ! eq (8)
         q4(lat,niz0:niz) = 2.63187e11 * mixCO2(niz0:niz)
     $                      * (1.-lamb_ext(niz0:niz))
     $                      * ( epst(n3) + phi(n3)
     $                         - EXP(aku/t_ext(niz0:niz)) )
     $                    / wmole(lat,niz0:niz)

!=======================================================================
!  ... Convert the heating rates from erg/g/s to K/day
!=======================================================================
         q4(lat,:) = - 86400. * 1.e-7 * q4(lat,:) / Cp(lat,:) 
         q3(lat,:) = - 86400. * 1.e-7 * q3(lat,:) / Cp(lat,:) 
                                                          
      end do    ! latitude loop

!=======================================================================

      CONTAINS
      
